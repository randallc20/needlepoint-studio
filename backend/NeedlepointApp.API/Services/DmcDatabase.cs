    public static DmcColor FindNearest(int r, int g, int b)
    {
        DmcColor? nearest = null;
        long minDist = long.MaxValue;

        foreach (var color in Colors)
        {
            long dr = color.R - r;
            long dg = color.G - g;
            long db = color.B - b;
            long dist = dr * dr + dg * dg + db * db;
            if (dist < minDist)
            {
                minDist = dist;
                nearest = color;
            }
        }

        return nearest!;
    }
